package com.tp.beans;

public class Etudiant {
    private Long mat;
    private String nom;
    private String prenom;

    public Long getMat() {
        return mat;
    }
    public void setMat( Long mat ) {
        this.mat = mat;
    }

    public String getNom() {
        return nom;
    }
    public void setNom( String nom ) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }
    public void setPrenom( String prenom ) {
        this.prenom = prenom;
    }

}
